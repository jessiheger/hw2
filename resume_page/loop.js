// 4. loop that logs the EVEN numbers from 0-98 (ascending)

function evenAsc() {
for (let i = 0; i <=98; i++){
	if (i % 2 === 0) {
		console.log(i);
	}
}
}

evenAsc();

// 5. loop that logs the EVEN numbers from 98-0 (descending)

function evenDes() {
	for (let i = 98; i >= 0; i--) {
		if (i % 2 === 0){
			console.log(i);
		}
	}
}

evenDes();

